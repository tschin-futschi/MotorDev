// =============================================================================
// @file    scopeviewmode.h
// @brief   示波器视图模式枚举定义
//
// 定义示波器波形显示的两种视图模式，供 ScopePlotWidget 和 OscilloscopTab 共用。
// =============================================================================

#pragma once

/// @brief 示波器视图模式枚举
///
/// 控制波形绘制区域的布局方式。Overlay 模式下所有通道叠加在同一绘图区域；
/// Stacked 模式下每个通道占据独立的水平条带。
enum class ScopeViewMode {
    Overlay = 0,   ///< 叠加模式：所有通道共享同一坐标系，波形重叠显示
    Stacked = 1    ///< 分离模式：每个通道在独立的水平条带中绘制
};
